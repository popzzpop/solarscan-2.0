<script lang="ts">
  export let isVisible = false;
  export let onContactCapture: (contact: any) => void = () => {};

  let contactInput = '';
  let contactName = '';
  let isValid = false;
  let contactType = '';
  let isSubmitting = false;

  // Smart contact detection
  function detectContactType(input) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    const phoneRegex = /^[\+]?[(]?[\d\s\-\(\)\.]{10,}$/;
    
    if (emailRegex.test(input.trim())) return 'email';
    if (phoneRegex.test(input.replace(/\s/g, ''))) return 'phone';
    return '';
  }

  function validateInput() {
    contactType = detectContactType(contactInput);
    isValid = contactType !== '' && contactInput.trim().length > 0;
  }

  function handleInput(event) {
    contactInput = event.target.value;
    validateInput();
  }

  function handleSubmit() {
    if (!isValid || isSubmitting) return;
    
    isSubmitting = true;
    
    const contact = {
      type: contactType,
      value: contactInput.trim(),
      name: contactName.trim() || extractNameFromEmail(contactInput),
      timestamp: new Date().toISOString(),
      source: 'manual'
    };
    
    onContactCapture(contact);
  }

  function extractNameFromEmail(email) {
    if (contactType === 'email') {
      const username = email.split('@')[0];
      return username.replace(/[._-]/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
    }
    return '';
  }

  function handleSocialLogin(provider) {
    isSubmitting = true;
    
    // In a real app, this would trigger actual OAuth flow
    const contact = {
      type: 'social',
      provider: provider,
      value: `${provider}_user_${Date.now()}`,
      name: `${provider} User`,
      timestamp: new Date().toISOString(),
      source: 'social'
    };
    
    onContactCapture(contact);
  }

  function handleSkip() {
    const contact = {
      type: 'anonymous',
      value: 'anonymous_user_' + Date.now(),
      name: 'Anonymous User',
      timestamp: new Date().toISOString(),
      source: 'skip'
    };
    
    onContactCapture(contact);
  }

  function handleKeydown(event) {
    if (event.key === 'Enter' && isValid) {
      handleSubmit();
    } else if (event.key === 'Escape') {
      handleSkip();
    }
  }

  // Auto-focus first field when modal opens
  let inputRef;
  $: if (isVisible && inputRef) {
    setTimeout(() => inputRef.focus(), 100);
  }
</script>

